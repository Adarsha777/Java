package com.basicJava.class5;

public class UnaryOperatorDemo {
	public static void main(String[] args) {
		
		int num1=10;
		System.out.println("Before Increament: "+num1);
		
		++num1;
		System.out.println("After Increament: "+num1);
		
		num1++;
		System.out.println("final Increament: "+num1);
		
		boolean flag=true;
		System.out.println("final: "+! flag);
	}

}
